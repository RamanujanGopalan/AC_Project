// ── device helpers ────────────────────────────────────────────────────────────
__device__ void aes_subBytes(uint8_t *state) {
    for (int i = 0; i < 16; i++) state[i] = d_sbox[state[i]];
}

__device__ void aes_shiftRows(uint8_t *s) {
    uint8_t temp;
    temp=s[1]; s[1]=s[5]; s[5]=s[9]; s[9]=s[13]; s[13]=temp;
    temp=s[2]; s[2]=s[10]; s[10]=temp;
    temp=s[6]; s[6]=s[14]; s[14]=temp;
    temp=s[3]; s[3]=s[15]; s[15]=s[11]; s[11]=s[7]; s[7]=temp;
}

__device__ uint8_t aes_xtime(uint8_t x) {
    return (x << 1) ^ ((x >> 7) * 0x1b);
}

__device__ void aes_mixColumns(uint8_t *s) {
    for (int i = 0; i < 4; i++) {
        uint8_t *col = s + i * 4;
        uint8_t t = col[0] ^ col[1] ^ col[2] ^ col[3];
        uint8_t tmp = col[0];
        col[0] ^= t ^ aes_xtime(col[0] ^ col[1]);
        col[1] ^= t ^ aes_xtime(col[1] ^ col[2]);
        col[2] ^= t ^ aes_xtime(col[2] ^ col[3]);
        col[3] ^= t ^ aes_xtime(col[3] ^ tmp);
    }
}

__device__ void aes_addRoundKey(uint8_t *state, const uint8_t *rk) {
    for (int i = 0; i < 16; i++) state[i] ^= rk[i];
}

// ── kernel ────────────────────────────────────────────────────────────────────
__global__ void aes_encrypt_kernel(uint8_t *input, uint8_t *output, int blocks) {
    int id = blockIdx.x * blockDim.x + threadIdx.x;
    if (id >= blocks) return;

    uint8_t state[16];
    for (int i = 0; i < 16; i++) state[i] = input[id * 16 + i];

    aes_addRoundKey(state, d_aes_roundKeys);

    for (int round = 1; round < AES_ROUNDS; round++) {
        aes_subBytes(state);
        aes_shiftRows(state);
        aes_mixColumns(state);
        aes_addRoundKey(state, d_aes_roundKeys + round * 16);
    }

    aes_subBytes(state);
    aes_shiftRows(state);
    aes_addRoundKey(state, d_aes_roundKeys + AES_ROUNDS * 16);

    for (int i = 0; i < 16; i++) output[id * 16 + i] = state[i];
}

__global__ void aes_ctr_kernel(uint8_t *out, const uint8_t *in,
                                size_t blocks, uint64_t nonce, uint64_t counter_start)
{
    size_t i = blockIdx.x * blockDim.x + threadIdx.x;
    if (i >= blocks) return;

    // Build counter block: [nonce (8 bytes) | counter (8 bytes)]
    uint8_t ctr_block[16];
    uint64_t ctr = counter_start + i;

    // Write nonce into bytes 0-7, counter into bytes 8-15 (big-endian)
    for (int b = 7; b >= 0; b--) {
        ctr_block[b]   = (nonce >> (8 * (7 - b))) & 0xFF;
        ctr_block[8+b] = (ctr   >> (8 * (7 - b))) & 0xFF;
    }

    // Encrypt the counter block to produce keystream
    aes_addRoundKey(ctr_block, d_aes_roundKeys);
    for (int round = 1; round < AES_ROUNDS; round++) {
        aes_subBytes(ctr_block);
        aes_shiftRows(ctr_block);
        aes_mixColumns(ctr_block);
        aes_addRoundKey(ctr_block, d_aes_roundKeys + round * 16);
    }
    aes_subBytes(ctr_block);
    aes_shiftRows(ctr_block);
    aes_addRoundKey(ctr_block, d_aes_roundKeys + AES_ROUNDS * 16);

    // XOR keystream with plaintext
    for (int j = 0; j < 16; j++)
        out[i * 16 + j] = in[i * 16 + j] ^ ctr_block[j];
}


