static uint8_t Rcon[11] = {0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36};

static void aes_keyExpansion(const uint8_t *key, uint8_t *roundKeys) {
    for (int i = 0; i < 16; i++) {
        roundKeys[i] = key[i];
    }
    int bytesGenerated = 16, rconIter = 1;
    uint8_t temp[4];
    while (bytesGenerated < 176) {
        for (int i = 0; i < 4; i++){
            temp[i] = roundKeys[bytesGenerated - 4 + i];
        }
        if (bytesGenerated % 16 == 0) {
            uint8_t t = temp[0]; temp[0]=temp[1]; temp[1]=temp[2]; temp[2]=temp[3]; temp[3]=t;
            for (int i = 0; i < 4; i++) {
                temp[i] = h_sbox[temp[i]];
            }
            temp[0] ^= Rcon[rconIter++];
        }
        for (int i = 0; i < 4; i++) {
            roundKeys[bytesGenerated] = roundKeys[bytesGenerated - 16] ^ temp[i];
            bytesGenerated++;
        }
    }
}

// ── public entry points (called from main.cu) ─────────────────────────────────

void cipher_aes_setup(const uint8_t *key) {
    uint8_t roundKeys[176];
    aes_keyExpansion(key, roundKeys);
    cudaMemcpyToSymbol(d_aes_roundKeys, roundKeys, 176);
}

void cipher_aes_launch(uint8_t *d_in, uint8_t *d_out, int blocks, int threads) {
    int grid = (blocks + threads - 1) / threads;
    aes_encrypt_kernel<<<grid, threads>>>(d_in, d_out, blocks);
}

void cipher_aes_launch_ctr(uint8_t *d_in, uint8_t *d_out,
                            int blocks, int threads,
                            uint64_t nonce, uint64_t counter_start) {
    int grid = (blocks + threads - 1) / threads;
    aes_ctr_kernel<<<grid, threads>>>(d_out, d_in, blocks, nonce, counter_start);
}
